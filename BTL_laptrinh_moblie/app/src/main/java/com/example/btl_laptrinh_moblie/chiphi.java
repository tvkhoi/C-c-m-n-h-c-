package com.example.btl_laptrinh_moblie;

public class chiphi {
    int icon;
    String ten;

    public chiphi(int icon, String ten) {
        this.icon = icon;
        this.ten = ten;
    }

    public int getIcon() {
        return icon;
    }

    public String getTen() {
        return ten;
    }

    public void setIcon(int icon) {
        this.icon = icon;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }
}
